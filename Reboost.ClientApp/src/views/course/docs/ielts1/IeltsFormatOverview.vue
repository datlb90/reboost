      <li><strong>IELTS General Training</strong>: Dành cho những người di cư đến các quốc gia nói tiếng Anh (ví dụ: Anh, Úc, Canada) hoặc đăng ký học trung học, chương trình đào tạo, hoặc kinh nghiệm làm việc.</li>
